# data_types_example.py
# Shows different data types (int, float, str, bool) and how to check them
# Students can run this to understand data types and the type() function

# Examples of different data types
book_title = "Python for Beginners"  # String
pages = 200  # Integer
price = 29.99  # Float
in_stock = True  # Boolean

# Check and print the type of each variable
print("Variable 'book_title' is a", type(book_title))
print("Variable 'pages' is a", type(pages))
print("Variable 'price' is a", type(price))
print("Variable 'in_stock' is a", type(in_stock))

# Practical example: Describe a book using all data types
print("\nBook Details:")
print("Title:", book_title)
print("Pages:", pages)
print("Price: $", price)
print("Available:", in_stock)