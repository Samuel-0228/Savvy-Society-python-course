# variables_example.py
# Demonstrates how to create and use variables in Python
# Students can run this to see how variables store different types of data

# Assigning values to variables
player_name = "Alex"  # String: stores the player's name
score = 100  # Integer: stores the player's score
health = 75.5  # Float: stores the player's health percentage
is_alive = True  # Boolean: indicates if the player is alive

# Printing variables to see their values
print("Player:", player_name)
print("Score:", score)
print("Health:", health, "%")
print("Is the player alive?", is_alive)

# Practical example: Update score and print a message
score = score + 50  # Increase score by 50
print("New score after bonus:", score)
